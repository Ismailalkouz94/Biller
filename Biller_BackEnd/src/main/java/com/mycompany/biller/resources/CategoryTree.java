package com.mycompany.biller.resources;

import org.springframework.hateoas.ResourceSupport;

/**
 *
 * @author DELL
 */
public class CategoryTree extends ResourceSupport {

    private Object LVL;
    private Object leaf;
    private Object categoryId;
    private Object parentCategoryId;
    private String description;

    public Object getLVL() {
        return LVL;
    }

    public void setLVL(Object LVL) {
        this.LVL = LVL;
    }

    public Object getLeaf() {
        return leaf;
    }

    public void setLeaf(Object leaf) {
        this.leaf = leaf;
    }

    public Object getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Object categoryId) {
        this.categoryId = categoryId;
    }

    public Object getParentCategoryId() {
        return parentCategoryId;
    }

    public void setParentCategoryId(Object parentCategoryId) {
        this.parentCategoryId = parentCategoryId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
